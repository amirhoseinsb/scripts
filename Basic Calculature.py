
n1 = int(input("Please Enter The Hour Number : "))
operator = str(input("Please Enter The Operator : "))
n2 = int(input("Please Enter The Minute Number : "))
if operator == "+" :
    print(n1+n2)
elif operator == "-" :
    print(n1-n2)
elif operator == "/" :
    print(n1/n2)
elif operator == "*" :
    print(n1*n2)
elif operator == "**" :
    print(n1**n2)
elif operator == "//" :
    print(n1//n2)
